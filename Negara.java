public class Negara {
    private String namaNegara;
    private String benua;

    public Negara(String namaNegara, String benua) {
        this.namaNegara = namaNegara;
        this.benua = benua;
    }

    public String getNamaNegara() {
        return namaNegara;
    }

    public String getBenua() {
        return benua;
    }
}